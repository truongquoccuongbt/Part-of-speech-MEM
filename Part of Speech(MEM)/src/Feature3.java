
public class Feature3 {
	private String tokenCurr;
	private String tokenAfter;
	
	public Feature3(String tokenCurr, String tokenAfter) {
		this.tokenCurr = tokenCurr;
		this.tokenAfter = tokenAfter;
	}

	public String getTokenCurr() {
		return tokenCurr;
	}

	public void setTokenCurr(String tokenCurr) {
		this.tokenCurr = tokenCurr;
	}

	public String getTokenAfter() {
		return tokenAfter;
	}

	public void setTokenAfter(String tokenAfter) {
		this.tokenAfter = tokenAfter;
	}
}
